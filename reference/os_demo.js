const os = require('os');

//Platform
console.log(os.platform());

// Cpu Arch
console.log(os.arch());

// Cpu core info
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// Total Mem
console.log(os.totalmem());

// Home dir
console.log(os.homedir());

// up time
console.log(os.uptime());